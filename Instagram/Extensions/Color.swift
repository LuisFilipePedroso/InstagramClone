import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    
    let accent = Color("Accent")
    let background = Color("Background")
    let border = Color("Border")
    let like = Color("Like")
    let link = Color("Link")
    let primaryText = Color("PrimaryText")
    let secondaryText = Color("SecondaryText")
    let surface = Color("Surface")
}
